
public class CharactertoInt {

    public static void main(String args[]) {
        char ch = 'a';
        int a = ch;
        System.out.println(a);
    }
}
/*
 * type promotion in expression: this is a special aspect in java
 * that is whenever you are going to use a char in expression it will
 * automatically
 * convert it to int. like that conversion of one datatype to another datatype
 * takesplace
 */