
public class Switch {

    public static void main(String[] args) {
        int n = 2;
        switch (n) {
            case 1:
                System.out.println("samosa");
                break;
            case 2:
                System.out.println("burger");
                break;
            case 3:
                System.out.println("Mango juice");
                break;
            default:
                System.out.println("Wakeup");
        }
    }
}
