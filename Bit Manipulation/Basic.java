
public class Basic {

    public static void main(String args[]) {
        System.out.println(5 & 6);// bitwise and operator
        System.out.println(5 | 6);// bitwise or operator
        System.out.println(~0);// bitwise xor operator
        System.out.println(2 ^ 3);// bitwise one's complement operator
        System.out.println(2 >> 2);// bitwise right shift operator
    }
}
